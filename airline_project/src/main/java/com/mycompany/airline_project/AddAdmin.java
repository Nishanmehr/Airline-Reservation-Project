            // TODO add your handling code here:
            String AdminId = adminID.getText();
            String FirstName = firstname.getText();
            String LastName = lastname.getText();
            String Password = password.getText();
            String UserName = username.getText();
          
           
            
            
            
            Connection con;
            PreparedStatement pre;
            ResultSet rs;
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            String dbPassword = System.getenv("DB_PASSWORD");
            con = DriverManager.getConnection(
                    "jdbc:mysql://localhost/Airline_Project",
                    "root",
                    dbPassword
            );
            pre=con.prepareStatement("insert into admin(AdminID,FirstName,LastName,Password,UserName)values(?,?,?,?,?)");
            pre.setString(1, AdminId);
            pre.setString(2, FirstName);
            pre.setString(3, LastName);
            pre.setString(4, Password);
            pre.setString(5, UserName);
            
