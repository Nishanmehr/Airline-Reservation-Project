     public void AutoID() {
     try {
        Connection con;
        PreparedStatement pre;
        ResultSet rs;

        Class.forName("com.mysql.cj.jdbc.Driver");

        String dbPassword = System.getenv("DB_PASSWORD");
        con = DriverManager.getConnection(
                "jdbc:mysql://localhost/Airline_Project",
                "root",
                dbPassword
        );

        pre = con.prepareStatement("SELECT MAX(AdminID) FROM admin");
        rs = pre.executeQuery();

        rs.next();

        if (rs.getString(1) == null) {
            adminID.setText("AD001");
        } else {
            String maxId = rs.getString(1); // e.g. CS005
            long id = Long.parseLong(maxId.substring(2)); // get 005 → 5
            id++;

            adminID.setText("AD" + String.format("%03d", id));
        }

    } 
       catch (ClassNotFoundException ex){
            System.getLogger(AddCustomer.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }catch (SQLException ex) {
            System.getLogger(AddCustomer.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
      }
     
}      

